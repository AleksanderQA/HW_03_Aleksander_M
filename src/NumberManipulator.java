public class NumberManipulator {

    int incrementByOne(int number) { // метод для перменной number
        number++; // уменьшение значения переменной на -1
        return number; // спросить на уроке


    }

    int decrementByOne (int number) { // метод для перменной number
        number--; // увеличение значения  переменной на +1
        return number; // спросить на уроке

    }
}
